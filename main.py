fin_Juego = False
while not fin_Juego:
    menu_Comienzo()
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
                fin_Juego = True     
        if event.type == pygame.KEYUP:
            if event.key == pygame.K_1:
                 main()
            if event.key == pygame.K_2:
                 instrucciones()
            if event.key == pygame.K_3:
                pygame.display.quit()
                fin_Juego = True

          
